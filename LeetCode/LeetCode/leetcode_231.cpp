/// <summary>
/// https://leetcode.com/problems/power-of-two/
/// </summary>
/// <returns></returns>
/// range a~b
/// 가장 적은 sorting으로 array의 모든 숫자를 cover하라.
/// 
class LeetCode_231 : public ISolution {
public:
    //2의 거듭제곱근인가?
    bool isPowerOfTwo(int n) {
        if (n <= 0) 
            return false;

        return (n & (n - 1)) == 0;
    }

    // ISolution을(를) 통해 상속됨
    virtual void Drive() override
    {
    }
};

